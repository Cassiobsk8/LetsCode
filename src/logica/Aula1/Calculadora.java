package logica.Aula1;

public class Calculadora
{
    public static float soma(float primeiroNumero, float segundoNumero)
    {
        return primeiroNumero + segundoNumero;
    }

    public static float mult(float primeiroNumero, float segundoNumero)
    {
        return primeiroNumero * segundoNumero;
    }

    public static float div(float primeiroNumero, float segundoNumero)
    {
        return primeiroNumero / segundoNumero;
    }

    public static float sub(float primeiroNumero, float segundoNumero)
    {
        return primeiroNumero - segundoNumero;
    }
}
